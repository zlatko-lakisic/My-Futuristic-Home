def mins(open_s, close_s):
    o = datetime.fromisoformat(open_s)
    c = datetime.fromisoformat(close_s)
    return round((c - o).total_seconds() / 60, 1)


def period_avg(runs, start, end):
    vals = [mins(a, b) for a, b in runs if start <= a[:10] <= end]
    return (round(sum(vals) / len(vals), 1), len(vals)) if vals else (None, 0)


def baseline_for_temp(temp_f):
    base = 10
    if temp_f >= 88:
        base += 3
    elif temp_f >= 80:
        base += 2
    elif temp_f <= 65:
        base -= 1
    return max(2, min(base, 25))


